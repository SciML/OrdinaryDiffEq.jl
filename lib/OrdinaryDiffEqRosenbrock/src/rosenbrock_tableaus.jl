    C[5, 2] = -convert(T, 15.1875)
    C[5, 3] = -convert(T, 4.03125)
    C[5, 4] = convert(T, 6.0)

    c = T2[convert(T2, 0), convert(T2, 4.0 / 9.0), convert(T2, 0), convert(T2, 1), convert(T2, 1)]
    d = T[convert(T, 1.0 / 3.0), -convert(T, 1.0 / 9.0), convert(T, 1.0), zero(T), zero(T)]
